namespace Daedalus.Data;

/// <summary>Where a Blue Mage spell is learned.</summary>
public enum BLUSpellSource
{
    /// <summary>Granted automatically at job unlock (Water Cannon).</summary>
    JobUnlock,

    /// <summary>Learned from an open-world mob in the named zone.</summary>
    OpenWorld,

    /// <summary>Bought with a Whalaqee Totem — Blue Mage job-quest reward.</summary>
    WhalaqeeTotem,

    /// <summary>Learned inside the named duty (synced clears guarantee the learn).</summary>
    Duty,
}

/// <summary>One spellbook entry: number, name, action id, and where it is learned.</summary>
